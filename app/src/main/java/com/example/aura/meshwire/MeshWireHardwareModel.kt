object MeshWireHardwareModel {

    fun wireCodeToName(code: Int): String = BY_CODE[code] ?: "UNKNOWN_$code"

    /** Обратное сопоставление для восстановления NodeDB с диска. */
    fun nameToWireCodeOrNull(name: String): Int? {
        val n = name.trim()
        if (n.isEmpty()) return null
        if (n.startsWith("UNKNOWN_", ignoreCase = true)) {
            return n.removePrefix("UNKNOWN_").removePrefix("unknown_").toIntOrNull()
        }
        return BY_CODE.entries.firstOrNull { it.value.equals(n, ignoreCase = true) }?.key
    }

